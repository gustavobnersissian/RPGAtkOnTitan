using System;
using System.Collections.Generic;
using System.Text;

namespace RPGAtackOnTitan
{
    class Status
    {
        public int força;
        public int defesa;
        public int agilidade;
        public int sorte;
        public int vida;
        public int vigor;

        public Status(int força, int defesa, int agilidade, int sorte, int vida, int vigor)
        {
            this.força = força;
            this.defesa = defesa;
            this.agilidade = agilidade;
            this.sorte = sorte;
            this.vida = vida;
            this.vigor = vigor;
        }

       
       
        
    }
}
